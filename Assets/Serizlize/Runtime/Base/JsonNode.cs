        public static bool operator ==(JsonNode a, object b)
        {
            if (ReferenceEquals(a, b))
                return true;
            bool aIsNull = a is JsonNull || ReferenceEquals(a, null) || a is JsonCreator;
            bool bIsNull = b is JsonNull || ReferenceEquals(b, null) || b is JsonCreator;
            if (aIsNull && bIsNull)
                return true;
            return !aIsNull && a.Equals(b);
        }

